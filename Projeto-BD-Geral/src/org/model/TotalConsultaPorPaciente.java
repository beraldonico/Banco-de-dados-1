package org.model;

import java.math.BigInteger;

public class TotalConsultaPorPaciente {
	
	private String nome = "";
	private BigInteger totalConsulta = null;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public BigInteger getTotalConsulta() {
		return totalConsulta;
	}
	public void setTotalConsulta(BigInteger totalConsulta) {
		this.totalConsulta = totalConsulta;
	}
	
	

}
